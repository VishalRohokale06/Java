class ScannerDemo{


        public static void main(String[] args){

                Scanner sc = new Scanner(System.in);
                String str = sc.next();

                int k =1;

                while(k<=3){
                        while(k<=3){
                                System.out.print(str+"\t");
                        }
                        System.out.println();
                }

        }
}
